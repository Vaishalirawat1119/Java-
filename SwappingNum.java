//Swapping 2 numbers using 3rd variable.

class SwappingNum {
    public static void main(String[] S) {
        int x = 11;
        int y = 19;
        int t;

        //Swapping x and y
        t = x;
        x = y;
        y = t;

        System.out.println("After Swapping x = " + x + " and y =  " +y);
        System.out.println();
    }
}
